package SegundaEvaluacion.Tema4.SuperHeroes;

public class SuperHeroe {
    String id;
    String nombre;
    String identidad;
    String descripcion;
    String imagen;

    public SuperHeroe(String id, String nombre, String identidad, String descripcion, String imagen) {
        this.id = id;
        this.nombre = nombre;
        this.identidad = identidad;
        this.descripcion = descripcion;
        this.imagen = imagen;
    }
}